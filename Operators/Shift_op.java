public class Shift_op {
    public static void main(String[] args) {
        /*
        Shift Operator    (3)         .....: << >> >>>
         */
        int a=8;
        System.out.println(a<<2); // 32 it will add two bits at end
        System.out.println(a>>2);//2 it will remove ast bit
        System.out.println(a>>>2);//fills with 0s
    }
}
