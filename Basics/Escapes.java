public class Escapes {
    public static void main(String[] args) {
        System.out.println("Mahendra\nhello\n How are you?");
        // \n for next lines
        System.out.println("Mahendra\thello\t");
        // \t for tab space that consist 8 spaces
        System.out.println("Mahendra\saHello");
        //\s for single space
    }
}
