public class Comments {
    public static void main(String[] args) {
        // 1. Single-line comment: starts with //
        System.out.println("This is a single-line comment example");

        /*
         2. Multi-line comment: starts with
        You can write multiple lines here.
        Useful for longer explanations.
        */
        System.out.println("This is a multi-line comment example");

        /**
         3. Documentation comment (Javadoc)
        Used to describe classes, methods, or fields.
                Tools like Javadoc can generate HTML documentation from these.
        */
        System.out.println("This is a documentation comment example");
    }
}
